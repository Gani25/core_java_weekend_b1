package com.sprk.day1;

public class Variable1 {

	public static void main(String[] args) {

		// Named container where we store data of defined typed
		// Variable -> data_type variable_name = value
		// camelCase -> studentFirstName
		byte number = 127;
		System.out.println("Byte number = " + number);
		
		int intNum = 21565656;
		System.out.println("Integer number = " + intNum);
		
		// Literal -> l/L
		long largeNumber = 150_00_000_000L;
		System.out.println("Long number = " + largeNumber);
		
		//	Decimal
		float pi = 3.14F;
		System.out.println("Float number = " + pi);
		
		double d = 1.9868;
		System.out.println("Double number = " + d);
		
		char alp = 'H';
		System.out.println("Character = " + alp);
		

	}

}
