package day4;

public class Inc_Dec {
	
	public static void main(String[] args) {
		int i = 10;
		
		int newNum = i++;
		
		System.out.println("Value of new Num = "+newNum);
		System.out.println("Value of i = "+i);
		int j = 10;
		
		int newNumJ = ++j;
		
		System.out.println("Value of new Num J = "+newNumJ);
		System.out.println("Value of j = "+j);
	}

}
